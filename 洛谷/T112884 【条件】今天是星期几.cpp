#include <bits/stdc++.h>
using namespace std;

int main() {
	
	int week;
	cin >> week;
	if(week == 1) {
		cout<<"Monday";
	}
	if(week == 2){
		cout<<"Tuesday"; 
	}
	if(week == 3){
		cout<<"Wednesday"; 
	}
	if(week == 4){
		cout<<"Thursday" ;
	}
	if(week == 5){
		cout<<"Friday" ;
	}
	if(week == 6){
		cout<<"Saturday"; 
	}
	if(week == 7){
		cout<<"Sunday";
	}
	return 0;
}
